void kjy_ahd_ch_ctrl(unsigned char _data)
{
	KJY_CURRENT_CHANNEL_MIRROR_CTRL();

	AHD_RegWrite(0xFF, 0x01);			//2 [JYKIM-2023-1-3]		bank 1
	AHD_RegWrite(0xc0, _data);
	MLD.MENU_LEVEL = 0;
}

BYTE kjy_ahd_ch_status(void)
{
	AHD_RegWrite(0xFF, 0x01);			//2 [JYKIM-2023-1-3]		bank 1
	return AHD_RegRead(0xc0)&0x0f;
}

