BYTE NVP1918C_GetVideoSystem(void)
{
	BYTE rVal;
	
	NVP1918C_RegWrite(0xFF, BANK1);
	rVal = NVP1918C_RegRead(0x08) & 0x1F;

	if (rVal==0x00) 	 return VDCNV_NTSC;
	else 				 return VDCNV_PAL;
}

