BYTE NVP1918C_GetVideoFormat(void)
{
	BYTE rVal = 0;

	if (rVal==0x00) 	 return VID_VENC_NTSC_M;
	else if (rVal==0x10) return VID_VENC_PAL_B;
	else if (rVal==0x20) return VID_VENC_SECAM;
	else if (rVal==0x30) return VID_VENC_NTSC_443;
	else if (rVal==0x40) return VID_VENC_PAL_M;
	else if (rVal==0x50) return VID_VENC_PAL_Nc;
	else if (rVal==0x60) return VID_VENC_PAL_60;
	else				 return VID_VENC_NOTVALID;
}

