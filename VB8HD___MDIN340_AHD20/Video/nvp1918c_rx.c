void nvp1918c_active_set(PNCDEC_INFO pINFO)
{

	NVP1918C_RegWrite( 0xFF,BANK3);

	if((pINFO->rx.resolution == NCRESOL_720X480) ||
	   (pINFO->rx.resolution == NCRESOL_720X576))
	{
		NVP1918C_RegWrite( 0x53,0x00);
		if(pINFO->rx.pllclk == PLL_27M)
		{
			NVP1918C_RegWrite( 0x62,0x00);
			NVP1918C_RegWrite( 0x63,0x00);
		}
		else
		{
			NVP1918C_RegWrite( 0x62,0x21);
			NVP1918C_RegWrite( 0x63,0xFF);
		}
	}
	else if((pINFO->rx.resolution == NCRESOL_960X480) ||
	        (pINFO->rx.resolution == NCRESOL_960X576))
	{
		NVP1918C_RegWrite( 0x53,0xFF);
		NVP1918C_RegWrite( 0x62,0x21);
		NVP1918C_RegWrite( 0x63,0x00);
	}

	NVP1918C_RegWrite( 0xFF,BANK4);
	NVP1918C_RegWrite( 0x70,0x00);
	NVP1918C_RegWrite( 0x71,0x00);
	NVP1918C_RegWrite( 0x72,0x00);
	NVP1918C_RegWrite( 0x73,0x00);
	NVP1918C_RegWrite( 0x74,0x00);

	switch(pINFO->rx.resolution) 
	{
		case NCRESOL_720X480 :
		case NCRESOL_720X576 :
		case NCRESOL_960X480 :
		case NCRESOL_960X576 :
			NVP1918C_RegWrite( 0x5C,0x00);
			NVP1918C_RegWrite( 0x5D,0x00);
			break;
		case NCRESOL_1280X480 :
		case NCRESOL_1280X576 :
			NVP1918C_RegWrite( 0x5C,0xAA);
			NVP1918C_RegWrite( 0x5D,0xAA);
			break;
		case NCRESOL_1440X480 :
		case NCRESOL_1440X576 :
			NVP1918C_RegWrite( 0x5C,0xFF);
			NVP1918C_RegWrite( 0x5D,0xFF);
			break;
	}									
	
}

