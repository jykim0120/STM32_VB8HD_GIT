void NVP1918C_ColorSet(PNCDEC_INFO pINFO )
{
	BYTE cnt;
	
	NVP1918C_RegWrite(0xFF,BANK0);

	// BRIGHITNESS (NTSC=0xFE, PAL=0xF8)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x0C+cnt,0xFE);
			NVP1918C_RegWrite(0x8C+cnt,0xFE);
		}
		else
		{
			NVP1918C_RegWrite(0x0C+cnt,0xF8);
			NVP1918C_RegWrite(0x8C+cnt,0xF8);
		}
	}

	// CONTRAST  (NTSC=0x88, PAL=0x80)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x10+cnt,0x88);
			NVP1918C_RegWrite(0x90+cnt,0x88);
		}
		else
		{
			NVP1918C_RegWrite(0x10+cnt,0x80);
			NVP1918C_RegWrite(0x90+cnt,0x80);
		}
	}
	
	// U_GAIN (NTSC 108M NT= 0x00, 108M PAL= 0x30,144M = 0x00 )
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x44+cnt,0x30);
			NVP1918C_RegWrite(0xC4+cnt,0x30);
		}
		else
		{
			NVP1918C_RegWrite(0x44+cnt,0x30);
			NVP1918C_RegWrite(0xC4+cnt,0x30);
		}
	}
	
	// V_GAIN (NTSC 108M NT= 0x30, 108M PAL= 0x00,144M = 0x00 )
	
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x48+cnt,0x30);	
			NVP1918C_RegWrite(0xC8+cnt,0x30);
		}
		else
		{
			NVP1918C_RegWrite(0x48+cnt,0x00);	
			NVP1918C_RegWrite(0xC8+cnt,0x00);
		}
	}

	// GAMMA setting  (NTSC 108M NT= 0xA4, 108M PAL= 0xA2,144M = 0x00 )
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x1C+cnt,0x24);
			NVP1918C_RegWrite(0x9C+cnt,0x24);
		}
		else
		{
			NVP1918C_RegWrite(0x1C+cnt,0x22);
			NVP1918C_RegWrite(0x9C+cnt,0x22);
		}
	}	

	// PAL_CM_OFF setting (NTSC=0x82, PAL=0x02)

	if(pINFO->vformat == NTSC)
	{
		NVP1918C_RegWrite(0x35,0x82);
		NVP1918C_RegWrite(0xB5,0x82);
	}
	else
	{
		NVP1918C_RegWrite(0x35,0x02);
		NVP1918C_RegWrite(0xB5,0x02);
	}
	
	// HUE setting (NTSC=0x01, PAL=0x00)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x40+cnt,0x01);
			NVP1918C_RegWrite(0xC0+cnt,0x01);
		}
		else
		{
			NVP1918C_RegWrite(0x40+cnt,0x00);
			NVP1918C_RegWrite(0xC0+cnt,0x00);
		}
	}
	
	// U_OFFSET setting (NTSC=0x00, PAL=0x04)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x4C+cnt,0x00);
			NVP1918C_RegWrite(0xCC+cnt,0x00);
		}
		else
		{
			NVP1918C_RegWrite(0x4C+cnt,0x04);
			NVP1918C_RegWrite(0xCC+cnt,0x04);
		}
	}

	// V_OFFSET setting (NTSC=0x00, PAL=0x04)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x50+cnt,0x00);
			NVP1918C_RegWrite(0xD0+cnt,0x00);
		}
		else
		{
			NVP1918C_RegWrite(0x50+cnt,0x04);
			NVP1918C_RegWrite(0xD0+cnt,0x04);
		}
	}

	// FLD_INV setting (NTSC=0xF1, PAL=0x01)
	if(pINFO->vformat == NTSC)
	{
		NVP1918C_RegWrite(0x54,0xF1);
		NVP1918C_RegWrite(0xD4,0xF1);
	}
	else
	{
		NVP1918C_RegWrite(0x54,0x01);
		NVP1918C_RegWrite(0xD4,0x01);
	}

	
	// VBLK_END setting (NTSC=0x08, PAL=0x0D)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)
		{
			NVP1918C_RegWrite(0x64+cnt,0x08);
			NVP1918C_RegWrite(0xE4+cnt,0x08);
		}
		else
		{
			NVP1918C_RegWrite(0x64+cnt,0x0D);
			NVP1918C_RegWrite(0xE4+cnt,0x0D);
		}
	}

	// 0x20 : BLACK_LEVEL (OLD = 0x84, NEW =0x80)
	// 0x25 : FSC LOCK_MODE & SPEED setting (NTSC=0xDC, PAL=0xCC)
	// 0x98 : COMB_TH_C (OLD = 0x80, NEW =0x80)
	// 0x99 : COMB_TH_D (OLD = 0x88, NEW =0x88)
	// 0xc1 : HPF_SLOPE_TH_P (OLD = 0x10, NEW =0x10)
	// 0xc2 : LPF_SLOPE_TH_P (OLD = 0x0E, NEW =0x0E)
	// 0xc3 : CYCLE_TH1 (OLD = 0x70, NEW =0x70)
	// 0xc7 : PREDICT_TH (OLD = 0x5C, NEW =0x5C)
	// 0xcb : CYCLE_TH (OLD = 0x40, NEW =0x40)
	// 0xcc : CYCLE_TH (OLD = 0x10, NEW =0x50)
	// 0xcd : CYCLE_SLP_TH1 (OLD = 0x08, NEW =0x08)
	// 0xce : PREDICT_SEL (OLD = 0x04, NEW =0x04)

	NVP1918C_RegWrite(0xFF,BANK3);
	if(pINFO->vformat == NTSC)
	{
		NVP1918C_RegWrite(0x20,0x84);	
		NVP1918C_RegWrite(0x25,0xDC);	
		NVP1918C_RegWrite(0x98,0x80);	
		NVP1918C_RegWrite(0x99,0x88);	
		NVP1918C_RegWrite(0xC1,0x30);	
		NVP1918C_RegWrite(0xC2,0x0C);	
		NVP1918C_RegWrite(0xC3,0x50);	
		NVP1918C_RegWrite(0xC7,0x50);	
		NVP1918C_RegWrite(0xCB,0x20);	
		NVP1918C_RegWrite(0xCC,0x60);	
		NVP1918C_RegWrite(0xCD,0x20);	
		NVP1918C_RegWrite(0xCE,0x14);	
	}
	else
	{
		NVP1918C_RegWrite(0x20,0x80);	
		NVP1918C_RegWrite(0x25,0xCC);	
		NVP1918C_RegWrite(0x98,0x80);	
		NVP1918C_RegWrite(0x99,0x88);	
		NVP1918C_RegWrite(0xC1,0x10);	
		NVP1918C_RegWrite(0xC2,0x0E);	
		NVP1918C_RegWrite(0xC3,0x70);	
		NVP1918C_RegWrite(0xC7,0x5C);	
		NVP1918C_RegWrite(0xCB,0x40);	
		NVP1918C_RegWrite(0xCC,0x50);	
		NVP1918C_RegWrite(0xCD,0x08);	
		NVP1918C_RegWrite(0xCE,0x04);	
	
	}
	//============== Bank 4 =====================//
	NVP1918C_RegWrite(0xff,BANK4);

	// DF_Y_DELAY (OLD=0x33, NEW=0x33)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC) 	NVP1918C_RegWrite(0x60+cnt,0x00);
		else						NVP1918C_RegWrite(0x60+cnt,0x33);
	}

	// DF_C_DELAY (OLD=0x55, NEW=0x55)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)	NVP1918C_RegWrite(0x64+cnt,0x00);
		else						NVP1918C_RegWrite(0x64+cnt,0xAA);
	}

	// DB_Y_DELAY (OLD=0x44, NEW=0x44)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)	NVP1918C_RegWrite(0x68+cnt,0x00);
		else						NVP1918C_RegWrite(0x68+cnt,0x77);
	}

	// DB_C_DELAY (OLD=0x33, NEW=0x33)
	for(cnt=0; cnt < 4; cnt++)
	{
		if(pINFO->vformat == NTSC)	NVP1918C_RegWrite(0x6C+cnt,0x00);
		else						NVP1918C_RegWrite(0x6C+cnt,0x33);
	}
}


